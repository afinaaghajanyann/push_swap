#include "push_swap.h"

void	s_func(t_struct **a)
{
	t_struct	*temp;

	if (!a || !(*a) || !(*a)->next)
		return ;
	temp = *a;
	*a = (*a)->next;
	temp->next = (*a)->next;
	(*a)->next = temp;
}
